/// This is not commutative.
inline
bool
Combine (
	const Combination * begin,
	const Combination * end,
	const char32_t c,
	char32_t & b
) {
	const Combination one = { c, b, -1U };
	const Combination * p(std::lower_bound(begin, end, one));
	if (p >= end || p->c != c || p->b != b) return false;
	b = p->r;
	return true;
}

