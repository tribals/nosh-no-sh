/// This is not Unicode composition; it is a means of typing dead key R (that is not in the common group 2) by typing two dead keys C1 and C2.
/// It is therefore commutative; whereas Unicode composition rules are not.
bool
combine_dead_keys (
	char32_t & c1,
	const char32_t c2
) {
	char32_t b(c2);
	if (Combine(peculiar_combinations1, peculiar_combinations1_end, c1, b)) {
		c1 = b;
		return true;
	}
	if (Combine(peculiar_combinations1, peculiar_combinations1_end, c2, c1)) {
		return true;
	}
	return false;
}

